export function getOfficialAuditSummary(items: OfficialAuditItem[] = OFFICIAL_AUDIT_ITEMS): OfficialAuditSummary {
  const summary: OfficialAuditSummary = {
    total: items.length,
    verified: 0,
    implemented: 0,
    partial: 0,
    open: 0,
    blockers: 0,
    completionRate: 0
  };

  for (const item of items) {
    summary[item.status]++;
    if (item.priority === 'critical' && item.status !== 'verified') {
      summary.blockers++;
    }
  }

  const weightedDone = summary.verified + summary.implemented * 0.75 + summary.partial * 0.4;
  summary.completionRate = summary.total > 0 ? Math.round((weightedDone / summary.total) * 100) : 0;
  return summary;
}

export function getOfficialAuditBlockers(items: OfficialAuditItem[] = OFFICIAL_AUDIT_ITEMS): OfficialAuditItem[] {
  return items.filter((item) => item.priority === 'critical' && item.status !== 'verified');
}
